/*
Write a program that declares variables
named init1, init2, and init3,
to hold your three initials.
Display the three initials with
a period following each one, as in J.M.F.

When submitting the program for
testing/grading,assign the values
J, M, and F to init1, init2, init3.
 */

public class Initials {
    public static void main(String[] args)
    {
        char init1, init2, init3;
        init1 = 'J';
        init2 = 'M';
        init3 = 'F';
        System.out.println("Here are the initials, "+init1+"."+init2+"."+init3+".");
    }
}
